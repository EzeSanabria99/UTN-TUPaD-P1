# ¿Qué es GitHub?
# 
# Es una plataforma online para guardar y compartir código usando Git.
# 
# ¿Cómo crear un repositorio en GitHub?
# 
# Vas a GitHub, hacés clic en "New", ponés nombre, elegís si es público o privado y creás el repo.
# 
# ¿Cómo crear una rama en Git?
# 
# git branch (insertar el nombre de la rama)
# 
# ¿Cómo cambiar a una rama en Git?
# 
# git checkout (insertar el nombre de la rama)
# 
# ¿Cómo fusionar ramas en Git?
# 
# Vas a la rama principal (master en mi caso) y poner: git merge (insertar el nombre de la rama)
# 
# ¿Cómo crear un commit en Git?
# 
# Primero agregás los cambios: git add .
# Después: git commit -m "mensaje del commit"
# 
# ¿Cómo enviar un commit a GitHub?
#
# git push origin (insertar el nombre de la rama)
#
# ¿Qué es un repositorio remoto?
#
# Es el repo que está en GitHub (o en otro servidor), no en tu compu.
#
# ¿Cómo agregar un repositorio remoto a Git?
#
# git remote add origin URL-del-repo
#
# ¿Cómo empujar cambios a un repositorio remoto?
#
# git push origin (insertar el nombre de la rama)
#
# ¿Cómo tirar de cambios de un repositorio remoto?
#
# # git pull origin (insertar el nombre de la rama)
#
# ¿Qué es un fork de repositorio?
#
# Es una copia de un repo ajeno en tu cuenta para modificarlo sin afectar el original.
#
# ¿Cómo crear un fork de un repositorio?
#
# Vas al repo en GitHub y hacés clic en el botón "Fork"
#
# ¿Cómo enviar una solicitud de extracción (pull request) a un repositorio?
#
# Subís tus cambios a una rama y en GitHub hacés clic en “Compare & pull request”.
#
# ¿Cómo aceptar una solicitud de extracción?
#
# En GitHub vas al pull request, revisás y hacés clic en “Merge pull request”.
#
# ¿Qué es una etiqueta en Git?
# 
# Es una marca para versiones específicas (como un punto de guardado importante).
#
# ¿Cómo crear una etiqueta en Git?
# 
# git tag (insertar nombre-etiqueta) 
#
# ¿Cómo enviar una etiqueta a GitHub?
#
# git push origin (insertar nombre-etiqueta) 
#
# ¿Qué es un historial de Git?
#
# Es el registro de todos los commits que hiciste en el proyecto.
#
# ¿Cómo ver el historial de Git?
#
# git log
#
# ¿Cómo buscar en el historial de Git?
#
# git log --grep="palabra clave" o git log -S"código"
#
# ¿Cómo borrar el historial de Git?
#
# No se borra fácil, pero podés resetear:
# git reset --hard commitID
#
# ¿Qué es un repositorio privado en GitHub?
#
# Es uno que solo vos (y quien invites) puede ver.
#
# ¿Cómo crear un repositorio privado en GitHub?
#
# En "New repository", tildás "Private" antes de crearlo.
#
# ¿Cómo invitar a alguien a un repositorio privado en GitHub?
#
# Vas a “Settings” > “Manage access” > “Invite collaborator”.
#
# ¿Qué es un repositorio público en GitHub?
#
# Cualquiera puede verlo, clonarlo o hacer fork.
#
# ¿Cómo crear un repositorio público en GitHub?
#
# En "New repository", tildás "Public".
#
# ¿Cómo compartir un repositorio público en GitHub?
#
# Copiás la URL del repo y se la pasás a quien quieras.
